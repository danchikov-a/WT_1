package task3.model;

public class InputData {
    private final int a;
    private final int b;
    private final int h;

    public InputData(int a, int b, int h) {
        this.a = a;
        this.b = b;
        this.h = h;
    }

    public int getA() {
        return a;
    }

    public int getB() {
        return b;
    }

    public int getH() {
        return h;
    }
}

